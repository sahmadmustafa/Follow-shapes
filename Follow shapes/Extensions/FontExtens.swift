import UIKit
extension UIFont{
    static var HKGrotesk: UIFont?{
        return UIFont(name: "HKGrotesk-Regular", size: 14)
    }
    
    static var HKGroteskB: UIFont?{
        return UIFont(name: "HKGrotesk-Bold", size: 14)
    }
    
    static var HKGroteskM: UIFont?{
        return UIFont(name: "HKGrotesk-Medium", size: 14)
    }
}
